public class Fahrenheit{
	public double fahrencalculator(double celcius){
	double fahrenheit = 9.0/5*celcius + 32;
	return fahrenheit;
	}

	public double celcalculator(double fahrenheit){
	double celsuis = 5.0/9*(fahrenheit - 32);
	return celsuis; 

	}
}